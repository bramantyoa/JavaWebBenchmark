    private void inURLFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_inURLFocusLost
        // TODO add your handling code here:
        String currentText = inURL.getText();
        if(currentText.equalsIgnoreCase("") || currentText.equalsIgnoreCase("http://")) {
            inURL.setText("http://");
            inURL.setForeground(Color.GRAY);
        }
    }//GEN-LAST:event_inURLFocusLost

    private void inURLFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_inURLFocusGained
        // TODO add your handling code here:
        String currentText = inURL.getText();
        if(currentText.equalsIgnoreCase("http://")) {
            inURL.setText("");
            inURL.setForeground(Color.BLACK);
        }
        
        
    }//GEN-LAST:event_inURLFocusGained

    private void btnStartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnStartMouseClicked
        // TODO add your handling code here:
        
        if(inURL.equals("http://") || inURL.equals("") || inRun.equals("") || inTest.equals("")) {
            this.labelErrText.setText("\nPlease specify the parameters.");
            this.dialogOut.setVisible(true);
        } else {
            this.textOut.append("\n\nNew Benchmarking Session\n");
            Benchmark bench = new Benchmark(this);
            bench.start();
            
        }
    }//GEN-LAST:event_btnStartMouseClicked

